public class TPC02 {
	public static void main(String[] args) {
		// 프로그래밍의 3대 요소: 변수, 자료형(DataType), 할당(=)
		// 1 + 1 = 2
		int a,b,c; // 변수 선언
		a = 1; //변수에 정수 할당
		b = 1;
		c = a+b; //2
		System.out.println(c);
		
		float f; //float, double
		f = 34.5f;
		System.out.println(f);
		
		char d;
		d = 'A';
		System.out.println(d);
		
//		Book bk; // Book class 설계하면됨 
	}
}
